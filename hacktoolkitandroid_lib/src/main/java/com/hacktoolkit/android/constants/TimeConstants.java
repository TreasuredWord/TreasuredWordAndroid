package com.hacktoolkit.android.constants;

public interface TimeConstants {
	public int MILLISECONDS_PER_SECOND = 1000;
	public int SECONDS_PER_MINUTE = 60;
	public int MINUTES_PER_HOUR = 60;
	public int HOURS_PER_DAY = 24;
	public int DAYS_PER_WEEK = 7;
	public int DAYS_PER_MONTH = 30;
	public int DAYS_PER_YEAR = 365;
	public int MONTHS_PER_YEAR = 12;
	
	public int ONE_SECOND_MILLISECONDS = MILLISECONDS_PER_SECOND;
	public int ONE_MINUTE_MILLIS = ONE_SECOND_MILLISECONDS * SECONDS_PER_MINUTE;
}
